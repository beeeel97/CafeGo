export class Cafeteria{


    IDCafeteria: number;
    NombreCafeteria: string;
    PropietarioCafeteria:  string;
    DireccionCafeteria:  string;


    constructor(
        IDCafeteria: number,
        NombreCafeteria: string,
        PropietarioCafeteria:  string,
        DireccionCafeteria:  string,
      ) {
        this.IDCafeteria = IDCafeteria;
        this.NombreCafeteria = NombreCafeteria;
        this.PropietarioCafeteria = PropietarioCafeteria;
        this.DireccionCafeteria = DireccionCafeteria;
      }
    

}